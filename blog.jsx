function mdToHtml(md) {
  const esc = (s) => s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
  const inline = (s) =>
    esc(s)
      .replace(/\*\*(.+?)\*\*/g, "<strong>$1</strong>")
      .replace(/\*(.+?)\*/g, "<em>$1</em>")
      .replace(/\[(.+?)\]\((.+?)\)/g, '<a href="$2">$1</a>');
  const lines = md.split("\n");
  let html = "", para = [];
  const flush = () => { if (para.length) { html += `<p>${inline(para.join(" "))}</p>`; para = []; } };
  for (const ln of lines) {
    const t = ln.trim();
    if (!t) { flush(); }
    else if (t.startsWith("## ")) { flush(); html += `<h2>${inline(t.slice(3))}</h2>`; }
    else if (t.startsWith("### ")) { flush(); html += `<h3>${inline(t.slice(4))}</h3>`; }
    else if (t.startsWith("> ")) { flush(); html += `<blockquote>${inline(t.slice(2))}</blockquote>`; }
    else { para.push(t); }
  }
  flush();
  return html;
}

